package desafios;

public class desafio02 {

	public static void main(String[] args) {
		
		int x, y, z;
		x = 10;
		y = 30;
		z = x + y;
		System.out.println("O valor de X é " + x);
		System.out.println("O valor de y é " + y);
		System.out.println("A soma de x + y é " + z);
		
		System.out.println();
		
		x = -30;
		y = 10;
		z = x + y;
		System.out.println("O valor de x é " + x);
		System.out.println("O valor de y é " + y);
		System.out.println("A soma de x + y é " + z);
		
		System.out.println();
		
		x = 0;
		y = 0;
		z = x + y;
		System.out.println("O valor de x é " + x);
		System.out.println("O valor de y é " + y);
		System.out.println("A soma de x + y é " + z);
		
		
	}

}
